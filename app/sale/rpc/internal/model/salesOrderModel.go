func (m *customSalesOrderModel) AddWithDetails(ctx context.Context, data *types.AddSalesOrderParam) error {
	err := m.conn.TransactCtx(ctx, func(ctx context.Context, session sqlx.Session) error {

		_, err := session.ExecCtx(ctx,
			`INSERT INTO sales_order (id,order_no, customer_id, order_date, promised_date, total_amount, status, 
			salesman_id, contract_url) VALUES (?, ?, ?, FROM_UNIXTIME(?), FROM_UNIXTIME(?), ?, ?, ?, ?)`,
			data.Id, data.OrderNo, data.CustomerId, data.OrderDate, data.PromisedDate, data.TotalAmount, data.Status, data.SalesmanId, data.ContractUrl,
		)
		if err != nil {
			return err
		}

		// insert details
		if len(data.Details) > 0 {
			// per row insert to keep it simple and clear
			for _, d := range data.Details {
				_, err := session.ExecCtx(ctx,
					`INSERT INTO sales_order_detail (id,order_id, product_id, product_name, unit, quantity, 
unit_price, amount, delivered_qty, remark) VALUES (?,?, ?, ?, ?, ?, ?, ?, ?, ?)`,
					d.Id, d.OrderId, d.ProductId, d.ProductName, d.Unit, d.Quantity, d.UnitPrice, d.Amount, d.DeliveredQty,
					d.Remark,
				)
				if err != nil {
					return err
				}
			}
		}
		return nil
	})
	return err
}
func (m *customSalesOrderModel) XUpdate(ctx context.Context, newData *SalesOrder) error {
	var setClauses []string
	var args []interface{}

	if newData.Status != 0 {
		setClauses = append(setClauses, "status = ?")
		args = append(args, newData.Status)
	}
	if newData.ContractUrl.String != "" {
		setClauses = append(setClauses, "contract_url = ?")
		args = append(args, newData.ContractUrl.String)
	}

	data, err := m.FindOne(ctx, newData.Id)
	if err != nil {
		return err
	}
	erpSaleSalesOrderIdKey := fmt.Sprintf("%s%v", cacheErpSalesSalesOrderIdPrefix, data.Id)
	erpSaleSalesOrderOrderNoKey := fmt.Sprintf("%s%v", cacheErpSalesSalesOrderOrderNoPrefix, data.OrderNo)
	_, err = m.ExecCtx(ctx, func(ctx context.Context, conn sqlx.SqlConn) (result sql.Result, err error) {
		query := fmt.Sprintf("update %s set %s where `id` = ?", m.table, strings.Join(setClauses, ", "))
		args = append(args, newData.Id)
		return conn.ExecCtx(ctx, query, args...)
	}, erpSaleSalesOrderIdKey, erpSaleSalesOrderOrderNoKey)
	return err
}


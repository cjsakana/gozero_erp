func (m *customSalesOrderModel) Search(ctx context.Context, data *types.SearchOrderParams) ([]*SalesOrder, int64, error) {
	var orders []*SalesOrder
	conditions := []string{}
	args := []any{}

	if data.OrderNo != "" {
		conditions = append(conditions, "order_no = ?")
		args = append(args, data.OrderNo)
	}
	if data.CustomerId != 0 {
		conditions = append(conditions, "customer_id = ?")
		args = append(args, data.CustomerId)
	}
	if data.Status != 0 {
		conditions = append(conditions, "status = ?")
		args = append(args, data.Status)
	}
	if data.SalesmanId != 0 {
		conditions = append(conditions, "salesman_id = ?")
		args = append(args, data.SalesmanId)
	}

	if !xtime.IsZeroTime(data.StartOrderDate) && !xtime.IsZeroTime(data.EndOrderDate) {
		conditions = append(conditions, "order_date BETWEEN ? AND ?")
		args = append(args, data.StartOrderDate, data.EndOrderDate)
	} else if !xtime.IsZeroTime(data.StartOrderDate) {
		conditions = append(conditions, "order_date >= ?")
		args = append(args, data.StartOrderDate)
	} else if !xtime.IsZeroTime(data.EndOrderDate) {
		conditions = append(conditions, "order_date <= ?")
		args = append(args, data.EndOrderDate)
	}

	base := fmt.Sprintf("select %s from %s", salesOrderRows, m.table)
	countBase := fmt.Sprintf("select count(*) from %s", m.table)
	if len(conditions) > 0 {
		where := " where " + strings.Join(conditions, " AND ")
		base += where
		countBase += where
	}

	var total int64
	if err := m.QueryRowNoCacheCtx(ctx, &total, countBase, args...); err != nil {
		return nil, 0, err
	}

	if data.Limit != -1 {
		base += fmt.Sprintf(" ORDER BY status LIMIT %d OFFSET %d", data.Limit, (data.Page-1)*data.Limit)
	}

	if err := m.QueryRowsNoCacheCtx(ctx, &orders, base, args...); err != nil {
		return nil, 0, err
	}
	return orders, total, nil
}
